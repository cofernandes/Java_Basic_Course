public class aula13 {
    
     public static void main(String[] args) {
         int resultado = 2 + 2;
         System.out.println(resultado);
         
         resultado = resultado - 1;
         System.out.println(resultado);
         
         resultado = resultado * 2;
         System.out.println(resultado);
         
         resultado = resultado + 8;
         System.out.println(resultado);
         
         resultado = resultado / 2;
         System.out.println(resultado);
         
         resultado = resultado % 7;
         System.out.println(resultado);
         
         resultado ++;
         System.out.println(resultado);
         
         System.out.println(resultado++);
         System.out.println(++resultado);
     }
    
}
